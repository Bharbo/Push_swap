int ft_iterative_factorial(int nb)
{
  int r;
  int i;
  
  i = 1;
  r = 1;
	while (i <= nb)
	{
		r *= i;
		i++;
	}
		return r;
	if (nb == 0)
		return 1;
	if (nb < 0 || nb >= 13)
		return 0;
}